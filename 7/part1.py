cpu = IntcodeCPU()

def run_amp(phase_settings, program):
    last_output = 0
    for phase_setting in phase_settings:
        cpu.reset()
        cpu.data_in = [phase_setting, last_output]
        cpu.run(program)
        last_output = cpu.output[0]
        # print(last_output)
    return last_output
    

